import java.util.Scanner;

public class KeepOn {

    public static void main(String args[]) 
    {
        int p = 0;

        while (p < 21) 
        {
            System.out.println("2 to the " + p + "th power is " + Math.pow(2, p));
            ++p;
            
        }
        
    }
}
